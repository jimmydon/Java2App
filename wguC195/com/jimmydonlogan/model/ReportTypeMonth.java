package com.jimmydonlogan.model;

/**
 * The type Report type month.
 */
public class ReportTypeMonth {
    /**
     * The Total month.
     */
    private int totalMonth;
    /**
     * The Type.
     */
    private String type;
    /**
     * The Month.
     */
    private String month;

    /**
     * Gets total month.
     *
     * @return the total month
     */
    public int getTotalMonth() {
        return totalMonth;
    }

    /**
     * Sets total month.
     *
     * @param totalMonth the total month
     */
    public void setTotalMonth(int totalMonth) {
        this.totalMonth = totalMonth;
    }

    /**
     * Gets type.
     *
     * @return the type
     */
    public String getType() {
        return type;
    }

    /**
     * Sets type.
     *
     * @param type the type
     */
    public void setType(String type) {
        this.type = type;
    }

    /**
     * Gets month.
     *
     * @return the month
     */
    public String getMonth() {
        return month;
    }

    /**
     * Sets month.
     *
     * @param month the month
     */
    public void setMonth(String month) {
        this.month = month;
    }
}
